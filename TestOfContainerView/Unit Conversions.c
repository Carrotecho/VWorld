#include "Unit Conversions.h"

/**
 Converts a degrees value to a radians value
 
 @param  degreesLong degrees
 @return radianLong radians
 */
radianLong convertDegreesToRadians(degreesLong degrees)
{
    // COMPLETED 4/14/2014: Unit tested successfully
    return (degrees * CONVERT_DEGREES_TO_RADIANS);
}

